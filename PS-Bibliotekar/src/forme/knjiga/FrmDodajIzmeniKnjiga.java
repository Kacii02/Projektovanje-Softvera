    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        String naslov = txtNaslov.getText().trim();
        String isbn = txtISBN.getText().trim();
        String autor = txtAutor.getText().trim();
        String izdavac = txtIzdavac.getText().trim();

        if (naslov.isEmpty() || isbn.isEmpty() || autor.isEmpty() || izdavac.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!", "Greška", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Knjiga novaKnjiga = new Knjiga(naslov, isbn, autor, izdavac);
        int uspeh = communication.dodajKnjigu(novaKnjiga);

        if (uspeh != 0) {
            JOptionPane.showMessageDialog(this, "Knjiga je uspešno dodata!", "Uspešno dodavanje", JOptionPane.INFORMATION_MESSAGE);
            parent.dobaviSveKnjige();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Došlo je do greške pri dodavanju knjige!", "Greška", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDodajActionPerformed

