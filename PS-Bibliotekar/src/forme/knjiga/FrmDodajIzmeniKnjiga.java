    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        String naslov = txtNaslov.getText().trim();
        String isbn = txtISBN.getText().trim();
        String autor = txtAutor.getText().trim();
        String izdavac = txtIzdavac.getText().trim();

        if (naslov.isEmpty() || isbn.isEmpty() || autor.isEmpty() || izdavac.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!", "Greška", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Knjiga izmenjenaKnjiga = new Knjiga(naslov, isbn, autor, izdavac);
        izmenjenaKnjiga.setIdKnjiga(knjigaZaIzmenu.getIdKnjiga());

        int uspeh = communication.izmeniKnjigu(izmenjenaKnjiga);

        if (uspeh != 0) {
            JOptionPane.showMessageDialog(this, "Knjiga je uspešno izmenjena.", "Uspešna izmena", JOptionPane.INFORMATION_MESSAGE);
            parent.dobaviSveKnjige();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Došlo je do greške pri izmeni knjige.", "Greška", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed


