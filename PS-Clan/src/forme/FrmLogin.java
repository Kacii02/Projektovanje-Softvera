    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String email = txtEmail.getText();
        String lozinka = String.valueOf(passLozinka.getPassword());
        if (email.equals("") || lozinka.equals("")) {
            JOptionPane.showMessageDialog(this, "Morate uneti sve podatke", "Nedostajuci kredencijali", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        Clan clan = new Clan();
        clan.setEmail(email);
        clan.setLozinka(lozinka);

        Clan ulogovani = communication.login(clan);

        if (ulogovani != null) {
            JOptionPane.showMessageDialog(this, "Uspesno logovanje");
            FrmClanGlavna frm = new FrmClanGlavna(ulogovani);
            frm.setVisible(true);
            
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Pokusajte ponovni unos kredencijala za prijavu", "Pogresni kredencijali", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

